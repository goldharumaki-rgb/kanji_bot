def get_hari_ke():
    mulai = datetime(2026, 6, 14)
    today = datetime.now()
    return (today - mulai).days

def pick_hari_ini(hari_ke):
    siklus = hari_ke % TOTAL_HARI
    k_start = (siklus * KANJI_PER_HARI) % TOTAL_KANJI
    v_start = (siklus * VOCAB_PER_HARI) % TOTAL_VOCAB
    kanji_list = [KANJI[(k_start + i) % TOTAL_KANJI] for i in range(KANJI_PER_HARI)]
    vocab_list = [VOCAB[(v_start + i) % TOTAL_VOCAB] for i in range(VOCAB_PER_HARI)]
    return kanji_list, vocab_list

def cek_selesai(hari_ke):
    siklus = hari_ke % TOTAL_HARI
    return siklus + 1 >= TOTAL_HARI

