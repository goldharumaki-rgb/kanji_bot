def buat_pesan_harian(hari_ke, kanji_list, vocab_list):
    today   = datetime.now()
    hari    = ["Senin","Selasa","Rabu","Kamis","Jumat","Sabtu","Minggu"][today.weekday()]
    tanggal = today.strftime("%d %B %Y")
    tip     = random.Random(today.year*10000+today.month*100+today.day+99).choice(TIPS)

    siklus = hari_ke % TOTAL_HARI
    k_done = min((siklus + 1) * KANJI_PER_HARI, TOTAL_KANJI)
    v_done = min((siklus + 1) * VOCAB_PER_HARI, TOTAL_VOCAB)
    k_pct  = int(k_done / TOTAL_KANJI * 100)
    v_pct  = int(v_done / TOTAL_VOCAB * 100)

    baris = []
    baris.append("🌸 *OHAYOU GOZAIMASU!* 🌸")
    baris.append(f"_{hari}, {tanggal}_")
    baris.append(f"Hari ke-*{siklus + 1}* dari *{TOTAL_HARI}* hari! ✨\n")
    baris.append("━━━━━━━━━━━━━━━━━━━━")
    baris.append(f"📖 *{KANJI_PER_HARI} KANJI HARI INI* (Kyouiku Kanji)")
    baris.append(f"Progress: {k_done}/{TOTAL_KANJI} ({k_pct}%) {'▓' * (k_pct // 10)}{'░' * (10 - k_pct // 10)}")
    baris.append("━━━━━━━━━━━━━━━━━━━━\n")
    for kanji, baca, arti, contoh, romaji, terjemah in kanji_list:
        baris.append(f"🔴 *{kanji}* — {baca}")
        baris.append(f"└ Arti: {arti}")
        baris.append(f"└ Contoh: {contoh}")
        baris.append(f"   ({romaji})")
        baris.append(f"   → {terjemah}\n")
    baris.append("━━━━━━━━━━━━━━━━━━━━")
    baris.append(f"💬 *{VOCAB_PER_HARI} KATA KERJA N3 HARI INI*")
    baris.append(f"Progress: {v_done}/{TOTAL_VOCAB} ({v_pct}%) {'▓' * (v_pct // 10)}{'░' * (10 - v_pct // 10)}")
    baris.append("━━━━━━━━━━━━━━━━━━━━\n")
    for kata, arti, contoh, romaji, terjemah in vocab_list:
        baris.append(f"🔵 *{kata}* — {arti}")
        baris.append(f"└ Contoh: {contoh}")
        baris.append(f"   ({romaji})")
        baris.append(f"   → {terjemah}\n")
    baris.append("━━━━━━━━━━━━━━━━━━━━")
    baris.append("📐 *POLA TATA BAHASA HARI INI*")
    baris.append("━━━━━━━━━━━━━━━━━━━━\n")
    pola_list = cari_pola_tata_bahasa(kanji_list, vocab_list)
    if pola_list:
        for nama, penjelasan in pola_list:
            baris.append(f"📌 *{nama}*")
            baris.append(f"{penjelasan}\n")
    else:
        baris.append("_Tidak ada pola tata bahasa khusus yang terdeteksi hari ini._\n")
    baris.append("━━━━━━━━━━━━━━━━━━━━")
    baris.append("💪 *TIPS HARI INI*")
    baris.append(f"_{tip}_\n")
    baris.append("がんばってください！ 🎌")
    return "\n".join(baris)

