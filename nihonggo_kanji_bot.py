KANJI_PER_HARI  = 4
VOCAB_PER_HARI  = 8

TOTAL_KANJI = len(KANJI)
TOTAL_VOCAB = len(VOCAB)

TOTAL_HARI_KANJI = -(-TOTAL_KANJI // KANJI_PER_HARI)
TOTAL_HARI_VOCAB = -(-TOTAL_VOCAB // VOCAB_PER_HARI)
TOTAL_HARI = max(TOTAL_HARI_KANJI, TOTAL_HARI_VOCAB)

def kirim_pesan(text):
    url  = f"https://api.telegram.org/bot{TOKEN}/sendMessage"
    data = json.dumps({"chat_id": CHAT_ID, "text": text, "parse_mode": "Markdown"}).encode()
    req  = urllib.request.Request(url, data=data, headers={"Content-Type": "application/json"})
    urllib.request.urlopen(req)

def get_hari_ke():
    mulai = datetime(2026, 6, 14)
    today = datetime.now()
    return (today - mulai).days

def pick_hari_ini(hari_ke):
    siklus = hari_ke % TOTAL_HARI
    k_start = (siklus * KANJI_PER_HARI) % TOTAL_KANJI
    v_start = (siklus * VOCAB_PER_HARI) % TOTAL_VOCAB
    kanji_list = [KANJI[(k_start + i) % TOTAL_KANJI] for i in range(KANJI_PER_HARI)]
    vocab_list = [VOCAB[(v_start + i) % TOTAL_VOCAB] for i in range(VOCAB_PER_HARI)]
    return kanji_list, vocab_list

def cek_selesai(hari_ke):
    siklus = hari_ke % TOTAL_HARI
    return siklus + 1 >= TOTAL_HARI

